# 2) strip()
# - 문자열 양 끝의 공백/개행(\n)/탭(\t) 등을 제거한다.
# - "중간 공백"은 제거하지 않는다.
# - 특정 문자를 지정해 제거할 수도 있다.

s1 = "   hello \n"
clean1 = s1.strip()         # "hello"

s2 = "###hello###"
clean2 = s2.strip("#")      # "hello"

s3 = "  a   b  "
clean3 = s3.strip()         # "a   b"  (중간 공백은 그대로)

# 3) split()
# - 문자열을 나눠서 리스트(list)로 만든다.
# - split() 기본: 공백(스페이스/탭/개행)을 기준으로 "연속 공백도 자동 처리"
# - split("구분자"): 지정한 구분자로 나눈다.

# (1) 기본 split()
line = "10   20  30"
parts = line.split()        # ["10", "20", "30"]  <- 문자열 리스트

# (2) 구분자 지정 split(",")
csv = "1,2,3,4"
parts2 = csv.split(",")     # ["1", "2", "3", "4"]

# (3) 주의: 구분자를 "정확히" 주면, 연속 구분자 처리 방식이 달라질 수 있음
line2 = "10  20"            # 공백 2개
line2.split()               # ["10", "20"]  (기본 split은 똑똑함)
line2.split(" ")            # ["10", "", "20"] (정확히 " " 하나로 나누면 빈 문자열 생김)


